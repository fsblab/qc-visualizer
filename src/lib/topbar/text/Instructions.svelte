<p>
    A circuit may consist of one or multiple components, a component may consist of other components. The elementary unit of a component is the gate. One may use predefined gates to create a component or their own custom components. When using custom components make sure not to create circular dependencies!
</p>
<p>
    Gates can be selected in the drop down menu or by pressing a button on the keyboard mapped to a specific gate. Once a gate is selected one can place it with left mouse button inside the component. The gate will be placed according to a grid layout to the nearest edge. Placing a gate on another gate will entirely overwrite that gate.
</p>

